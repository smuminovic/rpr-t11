package ba.unsa.etf.rpr;

public class Grad {
    private long id;
    private String naziv;
    private long gradId;

    public long getId() {
        return id;
    }

    public void setId(long id) {
        this.id = id;
    }

    public String getNaziv() {
        return naziv;
    }

    public void setNaziv(String naziv) {
        this.naziv = naziv;
    }

    public long getGradId() {
        return gradId;
    }

    public void setGradId(long gradId) {
        this.gradId = gradId;
    }
}
